# Son utilizadas para definir un valor iniciar o sobreescribir el valor de una variable.

#Atribuicao simples
# variable_name = value

saldo1 = 500
saldo1 = 100

print(saldo1)

#Atribuicao com adicao
# variable_name += value

saldo2 = 500
saldo2 += 100

print(saldo2)

#Atribuicao com subtracao
# variable_name -= value

saldo3 = 500
saldo3 -= 100

print(saldo3)

#Atribuicao com multiplicacao
# variable_name *= value

saldo4 = 500
saldo4 *= 2

print(saldo4)

#Atribuicao com divisao
# variable_name /= value

saldo5 = 500
saldo5 /= 2

print(saldo5)

